package by.bsuir.iba.core.enumerations;

/**
 * The enum {@code TrafficLightType} store possible
 * types of TrafficLight
 * <p>
 * Use
 * TrafficLightType.ROAD for road Traffic Light
 * TrafficLightType.PEDESTRIAN for pedestrian Traffic Light
 * TrafficLightType.BICYCLE for bicycle Traffic Light
 * </p>
 *
 * @author Pavel Vashkel
 * @see java.lang.Enum
 */
public enum TrafficLightType {
    ROAD,
    PEDESTRIAN,
    BICYCLE
}
