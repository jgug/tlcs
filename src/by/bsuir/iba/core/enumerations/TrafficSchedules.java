package by.bsuir.iba.core.enumerations;

/**
 * The enum {@code TrafficSchedules} store possible
 * types of road situations
 * <p>
 * Use
 * TrafficSchedules.BASIC for basic conditions
 * TrafficSchedules.NIGHT for night conditions
 * TrafficSchedules.JAM for hard road conditions
 * </p>
 *
 * @author Pavel Vashkel
 * @see java.lang.Enum
 */
public enum TrafficSchedules {
    BASIC,
    NIGHT,
    JAM
}
