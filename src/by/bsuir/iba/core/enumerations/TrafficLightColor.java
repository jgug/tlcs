package by.bsuir.iba.core.enumerations;

/**
 * The enum {@code TrafficLightColor} store possible
 * colors of TrafficLight
 * <p>
 * Use
 * TrafficLightColor.GREEN for green color
 * TrafficLightColor.AMBER for amber color
 * TrafficLightColor.RED for red color
 * </p>
 *
 * @author Pavel Vashkel
 * @see java.lang.Enum
 */
public enum TrafficLightColor {
    GREEN,
    AMBER,
    RED
}
